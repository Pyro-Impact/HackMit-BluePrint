//home page

function hideHome(){
    if(document.getElementById("homeSection").style.display==="block"){
    document.getElementById("homeSection").style.display="none";
    } else {
    document.getElementById("homeSection").style.display="block";
    }
}

//triviaSection
function hideTrivia(){
    if(document.getElementById("triviaSection").style.display==="block"){
    document.getElementById("triviaSection").style.display="none";
    } else {
    document.getElementById("triviaSection").style.display="block";
    }
}